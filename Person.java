class Person{
	private String name ;
	private Integer age ;
	private String card ;
	public void setName(String name){
		this.name = name ;
	}
	public String getName(){
		return this.name ;
	}
	public void setAge(Integer age){
		this.age = age ;
	}
	public Integer getAge(){
		return this.age ;
	}
	public void setCard(String card){
		this.card = card ;
	}
	public String getCard(){
		this.card = card ;
	}
}